def _parse_cmd_args() -> argparse.Namespace:
    """Parse the command line arguments."""
    description = (
        "Generate random, pronounceable words based on multigraph character-sequences "
        "and their probability based on an american-english dictionary."
    )
    parser = argparse.ArgumentParser(description=description)
    parser.add_argument(
        "word_length",
        action="store",
        nargs="?",
        default=5,
        type=int,
        help="the number of multigraph-sequences the resulting random word should have",
    )
    return parser.parse_args()


def _switch_char_type(curr_char_type: str) -> str:
    """Given the current character type, get the other one."""
    if curr_char_type == "CONSONANT":
        return "VOWEL"
    else:
        return "CONSONANT"


