    """Create a random, pronounceable word with the given number of
    multigraph-sequences. Randomly choose the multigraph-sequences according to
    their probability."""
    char_type = random.choice(["CONSONANT", "VOWEL"])
    start_keys = list(start[char_type].keys())
    start_values = list(start[char_type].values())
    word = random.choices(start_keys, start_values)[0]
    n_multigraphs -= 1

    while n_multigraphs > 1:
        char_type = _switch_char_type(char_type)
        mid_keys = list(mid[char_type].keys())
        mid_values = list(mid[char_type].values())
        word += random.choices(mid_keys, mid_values)[0]
        n_multigraphs -= 1

